class Example2 {
    public static void main(String[] args) {
        int num;

        num = 100;

        System.out.println("This is num: " + num);

        num = num * 2;

        System.out.println("This value of 2 * num: " + num);
    }
}
/**
 * Output:
 * -------------------- 
 * This is num: 100 
 * This value of 2 * num: 200
 */