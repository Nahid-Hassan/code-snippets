"""
    abs(num): The abs() method returns the absolute value of the given number. If the number is a complex number, abs() returns its magnitude.

    num:
        int, float, complex 
"""

int_number = -20
print(abs(int_number))

float_number = -30.33
print(abs(float_number))

complex_number = (3 - 4j)
print(abs(complex_number)) # 5